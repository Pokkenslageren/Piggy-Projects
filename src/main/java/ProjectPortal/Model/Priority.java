package ProjectPortal.Model;

/**
 * Represents the priority level of a task or process.
 * This enumeration is used to categorize tasks into different levels of urgency.
 * The priority levels include:
 * - LOW: Represents a low priority level.
 * - MEDIUM: Represents a medium priority level.
 * - HIGH: Represents a high priority level.
 */
public enum Priority {
    LOW, MEDIUM, HIGH
}
